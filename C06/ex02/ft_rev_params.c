#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	int	n;

	n = argc;
	while (n > 1)
	{
		ft_putstr(argv[--n]);
		write(1, "\n", 1);
	}
	return (0);
}
