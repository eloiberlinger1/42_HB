#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(int))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}

void	ft_lstdelone_no_funct(t_list *lst)
{
	if (!lst)
		return ;
	free(lst);
}
